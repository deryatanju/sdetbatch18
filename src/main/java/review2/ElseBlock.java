package review2;

public class ElseBlock {
    public static void main(String[] args) {
        int tem=91;
        if(tem>97){
            System.out.println("I will go to the beach");
        }else{
            System.out.println("I will stay at home and practise Java");
        }
        String month="November";
        if(month.equals("November")){
            System.out.println("It's thanksgiving holiday");
        }else{
            System.out.println("There are holidays");
        }
    }
}
